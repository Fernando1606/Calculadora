package calculadora;

public class operaciones {
	
	public int suma(int a, int b) {
		int suma;
		suma=a+b;
		return suma;
	}
	
	public int resta(int a, int b) {
		int resta;
		resta=a-b;
		return resta;
	}
	
	public int multiplicacion(int a, int b) {
		int mult;
		mult=a*b;
		return mult;
	}
	
	public double division(int a, int b) {
		double div;
		div=a/b;
		return div;
	}
}